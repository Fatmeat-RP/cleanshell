#include <minishell.h>

int	tote(char c, int q)
{
	if (q == 0)
	{
		if (c == '\'')
			q = 1;
		if (c == '\"')
			q = 2;
	}
	else if (q == 1)
	{
		if (c == '\'')
			q = 0;
		if (c == '\"')
			q = 2;
	}
	else if (q == 2)
	{
		if (c == '\'')
			q = 1;
		if (c == '\"')
			q = 0;
	}
	return (q);
}
